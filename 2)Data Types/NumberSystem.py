#Decimal -> Binary,octal,hexadecimal
x=20

y=bin(x)
print(y)

z=oct(x)
print(z)

t=hex(x)
print(t)

#Binary ->decimal,octal,hexadecimal
x=0b1100110101      #Binary Represenetation of a number  (Prefix: 0b)

y=x
print(y)

z=oct(x)
print(z)

t=hex(x)
print(t)

#Octal ->decimal,binary,hexadecimal
x=0o56237

y=x
print(y)

z=bin(x)
print(z)

t=hex(x)
print(t)


