# We can use type() function for getting data type of value 


# None
x=None
print(x)
print(type(x))

# Numeric: Foloowing are  data types for numerics

# 	int
x=10
print(x)
print(type(x))

# 	float
x=10.5
print(x)
print(type(x))

# 	complex     : the number of form (x+yj) is called complex number
x=10+20j
print(x)
print(type(x))

# 	boolean     True , False  are boolean variable
x=True
print(x)
print(type(x))

