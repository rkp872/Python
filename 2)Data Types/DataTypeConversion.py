# int->float

x=10    #int type
y=float(x)  
print(type(y)) #float

#float->int

x=10.56
y=int(x)
print(type(y))

#int -> Complex
x=10
y=20
z=complex(10,20)
print(type(z))

#int ->bool
x=1
y=bool(x)
print(type(y))

#bool ->int
x=True
y=int(x)
print(type(y))

#WE CAN CONVERT FROM ANY TYPE TO ANY TYPE USING FUNCTIONS LIKE THIS
