# Bitwise Operator
# ------------------
# complement ~
# and &
# or |
# xor  ^
# left shift  <<
# right shift >>


# complement (~)
x=12
y=~x
print(y)

# and (&)
x=10
y=12
z=x & y
print(z)

# or (|)
x=10
y=12
z=x | y
print(z)


# xor  (^)
x=10
y=12
z=x ^ y
print(z)

# left shift  (<<)
x=10
y=2
z=x << y # Shifts binary of x by y bits towards left
print(z)

# right shift (>>)
x=10
y=2
z=x >> y # # Shifts binary of x by y bits towards right
print(z)